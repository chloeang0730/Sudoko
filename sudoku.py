def value_by_single(board, i, j):
    """
    Input : board, row, and column index
    Output: The correct value for field (i, j) in board if it can be inferred as
            either a forward or a backward single; or None otherwise. 
    
    For example:

    >>> value_by_single(small2, 0, 1)
    2
    >>> value_by_single(small2, 0, 0)
    3
    >>> value_by_single(big, 0, 0)
    """
    v=options(board,i,j)
    
    #forward-single
    if len(v)==1:
        return v[0]

    #backward-single
    #row
    rolop=[]
    for r in range(len(board)):
        if board[i][r]==0:
            rolop+=options(board,i,r)
    
    
    colop=[]
    for c in range(len(board)):
        if board[c][j]==0:
            colop+=options(board,c,j)
    
    
    
    import math
    n=len(board)
    k=int(math.sqrt(n))
    lst=[]
    m=0
    l=0
    if i%k!=0:
        m=i%k
    else:
        m=0
    if j%k!=0:
        l=j%k
    else:
        l=0
    subop=[]
    for rol in range(i-m,i-m+k):
        for col in range(j-l,j-l+k):
            if board[rol][col]==0:
                subop+=options(board,rol,col)
    
    for a in v:
       if rolop.count(a)==1:
           return a
    for a in v:
        if colop.count(a)==1:
            return a
    for a in v:
        if subop.count(a)==1:
            return a
    return None


        
                   
                
            


        
        
        
    


